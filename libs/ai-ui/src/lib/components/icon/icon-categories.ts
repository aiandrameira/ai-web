const iconNames = Object.keys(AI_ICONS);

const categories: Record<IconCategory, string[]> = ICON_CATEGORY_ORDER.reduce((acc, category) => {
    acc[category] = [];
    return acc;
}, {} as Record<IconCategory, string[]>);

for (const icon of iconNames) {
    let matchedCategory: IconCategory | undefined;

    for (const category of ICON_CATEGORY_ORDER) {
        if (category === "Others") continue;

        const keywords = CATEGORY_KEYWORDS[category];
        if (keywords.some(keyword => icon.includes(keyword))) {
            matchedCategory = category;
            break;
        }
    }

    categories[matchedCategory ?? "Others"].push(icon);
}

for (const category of ICON_CATEGORY_ORDER) {
    categories[category] = [...new Set(categories[category])].sort();
}

export const ICON_CATEGORIES = categories;
